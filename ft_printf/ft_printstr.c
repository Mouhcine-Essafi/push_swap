#include "ft_printf.h"

int	ft_printstr(char *str, int i)
{
	if (str == NULL)
	{
		write (1, "(null)", 6);
		i += 6;
		return (i);
	}
	while (*str != '\0' )
	{
		i = ft_putchar(*str++, i);
	}
	return (i);
}
